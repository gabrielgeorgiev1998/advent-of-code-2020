  variations = []
  variations.append(tile[0])
  variations.append(np.flip(tile[0]))
  variations.append(tile[-1])
  variations.append(np.flip(tile[-1]))
  variations.append(tile[:,0])
  variations.append(np.flip(tile[:,0]))
  variations.append(tile[:,-1])
  variations.append(np.flip(tile[:,-1]))

  return variations

